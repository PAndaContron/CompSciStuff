public class Yorkshire extends Dog 
{
	private static int breedWeight = 75;
	
	//------------------------------------------------------------------------
	//	Constructor -- store name
	//------------------------------------------------------------------------
	public Yorkshire(String name)
	{
		super(name);
	}
	
	//------------------------------------------------------------------------
	//	Small bark -- overrides speak method in Dog
	//------------------------------------------------------------------------
	public String speak()
	{
		return "woof";
	}
	
	//------------------------------------------------------------------------
	//	Returns weight
	//------------------------------------------------------------------------
	public int avgBreedWeight()
	{
		return breedWeight;
	}
}
