public class MathMethods 
{

	public static void main(String[] args) 
	{
		System.out.println(1%5);
		System.out.println(5%2);
		System.out.println(7.2%3);
		System.out.println('A'+9);
		System.out.println(9.3%3);
		System.out.println('c'+5);
		System.out.println(Math.pow(3,3));
		System.out.println(Math.ceil(3.4));
		System.out.println(Math.floor(4.6));
		System.out.println(Math.sqrt(12));
		System.out.println(Math.round(12.34));
		System.out.println(Math.round(12.56));
		System.out.println(Math.max(12,34));
		System.out.println(Math.min(12,34));
		System.out.println(Math.max(12.3,45.6));
		System.out.println(Math.min(12.3,45.6));
		System.out.println(Math.abs(-213));
		System.out.println(Math.abs(213));
		System.out.println(Math.ceil(Math.sqrt(17)));
		System.out.println(Math.random());
		System.out.println(Math.random()*10);
		System.out.println(Math.random()*20+10);
	}

}
