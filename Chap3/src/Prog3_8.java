public class Prog3_8 
{

	public static void main(String[] args) 
	{
		//Variables
		final int LIMIT = 5;
		int count = 0;
		//Output
		while (count < LIMIT)
		{
			count++;
			System.out.println(count);
		}
		System.out.println("Done");
	}

}
