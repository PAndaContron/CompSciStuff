public class Prog1_4 
{

	public static void main(String[] args) 
	{
		//On one line
		System.out.println("Knowledge is power");
		System.out.println("");
		//On three lines, centered
		System.out.println("Knowledge");
		System.out.println("   is    ");
		System.out.println("  power  ");
		System.out.println("");
		//In a box
		System.out.println("====================");
		System.out.println("|Knowledge is power|");
		System.out.println("====================");

	}

}
